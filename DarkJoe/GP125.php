                        <ul>
                            <li class="fs-3 mar-bottom mar-top">
                                屋頂型:<br><br>將太陽光電模組設置在既有建物屋頂上，有效利用城市中已開發空間，主要考量的環境因素為遮陰問題，因為城市中高低不一的建物、水塔、女兒牆等，會造成不同日照時間、產生遮蔭，如何有效爭取日照面積及時長，是屋頂型光電最主要的評估；另外，因為多數建物居住人口或使用者不只一人，需要取得大樓住戶共識等，也是推進中會遭遇的困難。不過因為建置地點在城市中，輸配電網系統都已完善，較不會遇到電力基礎工程不足的問題。
                                依據如何將光電模組的支架結構，可分為<strong>平鋪式和棚架式。</strong><br><br>
                                <strong>平鋪式適用於斜屋頂、浪板屋頂建物，成本較低、組立快速有彈性</strong>；<strong>棚架式則適合多數平屋頂建築物</strong>。<br><img
                                    class="mar-top " src="assets/images/屋頂型.jpg" alt="屋頂型"><br><br>
                            </li>
                            <li class="fs-3 mar-bottom mar-top">地面型:<br><br>
                                地面型太陽光電為直接設置於陸域上的光電模組，如不利耕作農地、各式工業用地等。以其他國家發展過程如德國、法國、澳洲等國家因為土地幅遠遼闊，常常可見一大片壯闊的發電廠。台灣因為地狹人稠、生態環境豐富且脆弱，在發展地面型光電時，更要仔細評估各層面的優劣利弊。特別是近年政府主力推進能源政策，地方政府也祭出各式獎勵措施，部分業者可能藉由法規不全處，加速申請流程，後續造成農地破碎、環評不符資格而遭社會大眾不良觀感及檢討。
                                <br>排除這些需要檢討的案例，依據模組分為<strong>固定式與追日式</strong>：<br><br>
                                <strong>固定式模組</strong>如同屋頂型太陽光電，為<strong>計算整體日照、傾角、遮蔭等環境因素後，計算出最佳日照角度，固定於地面上</strong>；架高系統下可耕種需光較少的農作或溫室，亦有牲畜農舍等案例；<strong>追日式</strong>則為活動式機構，會<strong>隨日照角度轉動模組角度，以取得更好的發電效能，追日型系統比固定式系統多10%以上的系統成本，但是能額外增加10%～25%的發電量</strong>，不過因為單軸活動機構較固定式不耐強風，雖然廠商也積極研發更好的機構系統，以目前來說，<strong>固定式的環境風險相對較能預期</strong>。
                                <br><br><img src="assets/images/地面型.jpg" alt="地面型"><br>
                            </li><br>
                            <li class="fs-3 mar-bottom mar-top">
                                水面型:<br><br>水域型太陽光電是將設備設置於水面上或岸邊地區。水域型光電相較於陸域型兩種，需要考慮更多環境因素；除了基本的強風、降雨等，強風還會造成水浪、水花、高速水流、以及沖刷下來的漂流物等外力因素，部件因為長期潮濕，會增加腐蝕機率、降低模組壽命，以及該地區原有漁業也會受到影響。<br>
                                <br>根據選擇的支撐結構設置差異，分為<strong>固定式及浮動式（floating photovoltaic,
