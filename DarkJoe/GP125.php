                        <p class="fs-1 setcen">地熱能</p>
                        <p>地熱發電利用地球內部的高溫加熱地層水推動渦輪機發電。由於使用地球內部的熱源取代煤碳、天然氣或核燃料，是一種便宜且乾淨的發電系統。由於地下的熱源相當豐富，可以24小時連續發電，是一種穩定的再生能源，可以當作基載電力。
                        依照地熱流體在儲集層中的形式，地熱系統可分成熱水型和蒸氣型：</p>
                        <ol class="fs-2">
                            <li>
                                熱水型：占全世界的地熱系統10%。以熱水存在儲集層中，溫度最高可達攝氏370度，低者則只有數十度。攝氏200度以上的地熱系統，因為熱水到達地面後，可以有10% ~
                                30％轉變為蒸氣，所以比較有發電價值。
                            </li>
                            <li>
                                蒸氣型：占全世界的地熱系統90%。由地底高溫的熱源提供熱量，再加上岩層的低滲透性而形成。一開始原本是熱水型，水經高溫沸騰變成蒸氣，然後水位下降，又因熱水湧出量大於地下水注入量，慢慢轉變成蒸氣型。在蒸氣儲集層中是熱水與蒸氣共存。
                            </li>
                        </ol>
