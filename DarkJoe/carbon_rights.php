                <p style="font-size: 90px; color: #43a5a5;">碳權是什麼</p>
                <br>
                <h1><b>「碳權」（Carbon credit）</b></h1>
                <p>簡單來說就是「二氧化碳排放的權利」，基本上以
                    1公噸碳排放量為一計算單位，單位為二氧化碳當量（C02e）。不僅將碳排放量化讓國家或企業有依據計算減碳總量，同時也將碳權商品化，透過交易系統為碳排定價，屬於一種金融產品，同時達到「控制總量」也就是節能減碳的目的。碳權產生方式有兩種，包含強制性市場和自願性市場。
                </p>
            </div>
        </div>

