    <section id="services2" class="service-area">
        <div class="container">
            <div class="row" id="END">
                <p style="font-size: 90px; color: rgb(117 175 69);">碳交易市場是什麼</p>
                <p>
                    剛說到碳權產生方式可分為政府強制總量管制與交易（Cap and Trade）的強制性市場交易和自願性市場產生，其中總量管制是目前最常見的方式。

                    總量管制與交易（Cap and Trade）強制性市場
                    簡稱碳交易。政府訂定排放總量最高上限，並按產業發放「排放配額」給不同企業。碳排超出額度的企業須到碳交易市場購買碳權，彌補超額部分；
                    反之，排放量低於配額可將多出額度轉換成碳權，到碳交易市場交易獲利，全球首個碳交易機制「歐盟碳交易市場」（European Union Emission Trading System，EU
                    ETS）正是此種交易模式。
                    自願性市場
                    自願性市場是以碳抵換（Carbon Offset）作為交易工具，有 ESG 需求或立志減碳的企業以專案型式減碳、使用再生能源或碳捕捉、造林等，取得境內或是國際機構認證就能進行碳抵換。
                </p>
            </div>
        </div>


