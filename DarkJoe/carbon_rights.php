                <p style="font-size: 90px; color: #43a5a5;">碳稅碳費是什麼</p>
                <p>
                    碳稅、碳費（Carbon
                    Tax）是使用排放二氧化碳服務或商品時課徵稅額或徵收費用，兩者未有太大差異。而不同於碳交易市場，這兩項由政府「以價制量」直接訂價，藉由控制價格高底來影響碳排放多寡，目前收取碳稅國家包含英國、加拿大、北歐五國、新加坡、日本等，稅收可視為整體政府財政收入；台灣則是利用碳費，由環保署執行並將收入專款專用，預計
                    2024 年上路，屆時碳排大戶（含半導體、鋼鐵及用電加製程達標業者）收取碳費，這項收入會用於成立氣候基金，發展低碳與負排放產業、補助及獎勵投資溫室氣體減量技術等。</p>
